a = 10
b = 10

# sum/add operation
print('Addition:',a+b)

# difference/subtraction operation
print('Subtract:',a-b)

# product/multiplication operation
print('Product:',a*b)

# Division operation
print('Division:',a/b)

# Division and floor value
print('Floor division',a//b)

# Modulus
print('Modulus',a%b)

# Power
print('Power',a**2)


a,b = map(int, input('Enter 2 numbers:').split())

# greater than
if a > b:
    print(f'{a} is greater than {b}')

# greater than or equals
if a >= b:
    print(f'{a} is greater than or equal to {b}')

# lesser than
if a < b:
    print(f'{a} lesser than {b}')

# lesser than or equals
if a <= b:
    print(f'{a} lesser than or equals to {b}')

# equals
if a == b:
    print(f'{a} equals {b}')

# not equals
if a != b:
    print(f'{a} not equals {b}')